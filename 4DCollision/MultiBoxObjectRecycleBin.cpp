#include "MultiBoxObjectRecycleBin.h"
#include "MultiBoxObject.h"

///////////////////////////////////////////////////////////////////////////////
//
// Definition
//
///////////////////////////////////////////////////////////////////////////////

#define MAX_OBJECT_COUNT									2048

///////////////////////////////////////////////////////////////////////////////
//
// CMultiBoxObjectRecycleBin
//
///////////////////////////////////////////////////////////////////////////////

CMultiBoxObjectRecycleBin::CMultiBoxObjectRecycleBin(void)
{
	//Set default capacity.
	setCapacity(MAX_OBJECT_COUNT);
}

///////////////////////////////////////////////////////////////////////////////
//
// ~CMultiBoxObjectRecycleBin
//
///////////////////////////////////////////////////////////////////////////////

CMultiBoxObjectRecycleBin::~CMultiBoxObjectRecycleBin(void)
{
	//Clear all.
	clearAll();
}

///////////////////////////////////////////////////////////////////////////////
//
// NewObject
//
///////////////////////////////////////////////////////////////////////////////

_OBJECT CMultiBoxObjectRecycleBin::newObject()
{
	//Return result.
	return new CMultiBoxObject();
}

///////////////////////////////////////////////////////////////////////////////
//
// DeleteObject
//
///////////////////////////////////////////////////////////////////////////////

void CMultiBoxObjectRecycleBin::deleteObject(_OBJECT object)
{
#ifdef _DEBUG
	assert(object != _NULL);
#endif
	//Delete object.
	delete ((CMultiBoxObject *)object);
}

///////////////////////////////////////////////////////////////////////////////
//
// Recycle
//
///////////////////////////////////////////////////////////////////////////////

void CMultiBoxObjectRecycleBin::recycle(_OBJECT object)
{
#ifdef _DEBUG
	assert(object != _NULL);
#endif
	//Recycle box.
	CRecycleBin::recycle((CMultiBoxObject *)object);
}
