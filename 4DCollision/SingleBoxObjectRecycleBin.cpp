#include "SingleBoxObjectRecycleBin.h"
#include "SingleBoxObject.h"

///////////////////////////////////////////////////////////////////////////////
//
// Definition
//
///////////////////////////////////////////////////////////////////////////////

#define MAX_OBJECT_COUNT									2048

///////////////////////////////////////////////////////////////////////////////
//
// CSingleBoxObjectRecycleBin
//
///////////////////////////////////////////////////////////////////////////////

CSingleBoxObjectRecycleBin::CSingleBoxObjectRecycleBin(void)
{
	//Set default capacity.
	setCapacity(MAX_OBJECT_COUNT);
}

///////////////////////////////////////////////////////////////////////////////
//
// ~CSingleBoxObjectRecycleBin
//
///////////////////////////////////////////////////////////////////////////////

CSingleBoxObjectRecycleBin::~CSingleBoxObjectRecycleBin(void)
{
	//Clear all.
	clearAll();
}

///////////////////////////////////////////////////////////////////////////////
//
// NewObject
//
///////////////////////////////////////////////////////////////////////////////

_OBJECT CSingleBoxObjectRecycleBin::newObject()
{
	//Return result.
	return new CSingleBoxObject();
}

///////////////////////////////////////////////////////////////////////////////
//
// DeleteObject
//
///////////////////////////////////////////////////////////////////////////////

void CSingleBoxObjectRecycleBin::deleteObject(_OBJECT object)
{
#ifdef _DEBUG
	assert(object != _NULL);
#endif
	//Delete object.
	delete ((CSingleBoxObject *)object);
}

///////////////////////////////////////////////////////////////////////////////
//
// Recycle
//
///////////////////////////////////////////////////////////////////////////////

void CSingleBoxObjectRecycleBin::recycle(_OBJECT object)
{
#ifdef _DEBUG
	assert(object != _NULL);
#endif
	//Recycle box.
	CRecycleBin::recycle((CSingleBoxObject *)object);
}
