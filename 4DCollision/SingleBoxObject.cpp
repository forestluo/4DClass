#include "SingleBoxObject.h"
#include "SingleBoxObjectRecycleBin.h"

///////////////////////////////////////////////////////////////////////////////
//
// Global
//
///////////////////////////////////////////////////////////////////////////////

#pragma init_seg(".CRT$XCF")
//Recycle bin.
static CSingleBoxObjectRecycleBin singleBoxObjectBin;

///////////////////////////////////////////////////////////////////////////////
//
// CSingleBoxObject
//
///////////////////////////////////////////////////////////////////////////////

CSingleBoxObject::CSingleBoxObject(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// ~CSingleBoxObject
//
///////////////////////////////////////////////////////////////////////////////

CSingleBoxObject::~CSingleBoxObject(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// IsSingle
//
///////////////////////////////////////////////////////////////////////////////
	
_BOOL CSingleBoxObject::isSingle()
{
	//Return true.
	return _TRUE;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetTotalCount
//
///////////////////////////////////////////////////////////////////////////////

_INTEGER CSingleBoxObject::getTotalCount()
{
	//Return result.
	return 1;
}

///////////////////////////////////////////////////////////////////////////////
//
// Recycle
//
///////////////////////////////////////////////////////////////////////////////

void CSingleBoxObject::recycle()
{
	//Clear all.
	clearAll();
	//Recycle object.
	singleBoxObjectBin.recycle(this);
}

///////////////////////////////////////////////////////////////////////////////
//
// Create
//
///////////////////////////////////////////////////////////////////////////////

CSingleBoxObject* CSingleBoxObject::create()
{
	//Return result.
	return (CSingleBoxObject *)singleBoxObjectBin.create();
}
