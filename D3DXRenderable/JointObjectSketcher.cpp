// CJointObjectSketcher
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CJointObjectSketcher::CJointObjectSketcher(void)
{
	//Set default value.
	drawJointSphere = _FALSE;
	drawJointPosition = _FALSE;
	drawJointDirection = _FALSE;
}

