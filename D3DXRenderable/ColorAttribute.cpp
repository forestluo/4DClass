#include "ColorAttribute.h"

///////////////////////////////////////////////////////////////////////////////
//
// CColorAttribute
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CColorAttribute::CColorAttribute(void)
{
	//Set global color.
	globalColor = D3DCOLOR_ARGB(0,255,255,255);
	//Set light color.
	lightColor = D3DXVECTOR4(1.0f,1.0f,1.0f,1.0f);
}

///////////////////////////////////////////////////////////////////////////////
//
// CColorAttribute
//
// Default deconstruction.
//
/////////////////////////////////////////////////////////////////////////////////

CColorAttribute::~CColorAttribute(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// GetColor
//
///////////////////////////////////////////////////////////////////////////////

D3DCOLOR CColorAttribute::getColor() const
{
	//Return result.
	return D3DCOLOR(globalColor);
}

///////////////////////////////////////////////////////////////////////////////
//
// SetColor
//
///////////////////////////////////////////////////////////////////////////////

void CColorAttribute::setColor(const D3DCOLOR& color)
{
	//Set value.
	globalColor = color;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetLightColor
//
///////////////////////////////////////////////////////////////////////////////

D3DXVECTOR4 CColorAttribute::getLightColor() const
{
	//Return result.
	return D3DXVECTOR4(lightColor);
}

///////////////////////////////////////////////////////////////////////////////
//
// SetLightColor
//
///////////////////////////////////////////////////////////////////////////////

void CColorAttribute::setLightColor(const D3DXVECTOR4& color)
{
	//Set value.
	lightColor = color;
}
