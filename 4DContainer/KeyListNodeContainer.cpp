///////////////////////////////////////////////////////////////////////////////
//
// AddHead
//
///////////////////////////////////////////////////////////////////////////////

_OBJECT CKeyListNodeContainer::addHead(_LONG key,_OBJECT object)
{
#ifdef _DEBUG
	assert(key != -1);
#endif
	//Create node.
	CKeyListNode* node = new CKeyListNode(key,object);
#ifdef _DEBUG
	assert(node != _NULL);
#endif
	//Increase count.
	increaseCount();
	//Add head.
	addHeadNode(node);
	//Return result.
	return node;
}

///////////////////////////////////////////////////////////////////////////////
//
// AddTail
//
///////////////////////////////////////////////////////////////////////////////

_OBJECT CKeyListNodeContainer::addTail(_LONG key,_OBJECT object)
{
	//Create node.
	CKeyListNode* node = new CKeyListNode(key,object);
#ifdef _DEBUG
	assert(node != _NULL);
#endif
	//Increase count.
	increaseCount();
	//Add tail.
	addTailNode(node);
	//Return result.
	return node;
}
