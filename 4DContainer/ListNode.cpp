#include "ListNode.h"

////////////////////////////////////////////////////////////////////////////////
//
// CListNode
//
////////////////////////////////////////////////////////////////////////////////

CListNode::CListNode(void)
{
	//Set next.
	next = this;
	//Set previous.
	previous = this;
}

////////////////////////////////////////////////////////////////////////////////
//
// CListNode
//
////////////////////////////////////////////////////////////////////////////////

CListNode::CListNode(_OBJECT value)
	: C4DNode(value)
{
	//Set next.
	next = this;
	//Set previous.
	previous = this;
}

////////////////////////////////////////////////////////////////////////////////
//
// ~CListNode
//
////////////////////////////////////////////////////////////////////////////////

CListNode::~CListNode(void)
{
	//Set next.
	next = (CListNode *)_NULL;
	//Set previous.
	previous = (CListNode *)_NULL;
}

////////////////////////////////////////////////////////////////////////////////
//
// GetNext
//
////////////////////////////////////////////////////////////////////////////////

CListNode* CListNode::getNext()
{
	//Return result.
	return next;
}

////////////////////////////////////////////////////////////////////////////////
//
// GetPrevious
//
////////////////////////////////////////////////////////////////////////////////

CListNode* CListNode::getPrevious()
{
	//Return result.
	return previous;
}

////////////////////////////////////////////////////////////////////////////////
//
// SetNext
//
////////////////////////////////////////////////////////////////////////////////
	
void CListNode::setNext(CListNode* next)
{
	//Set next.
	this->next = next;
}

////////////////////////////////////////////////////////////////////////////////
//
// SetPrevious
//
////////////////////////////////////////////////////////////////////////////////
	
void CListNode::setPrevious(CListNode* previous)
{
	//Set previous.
	this->previous = previous;
}

////////////////////////////////////////////////////////////////////////////////
//
// SetNextPrevious
//
////////////////////////////////////////////////////////////////////////////////

void CListNode::setNextPrevious(CListNode* previous)
{
#ifdef _DEBUG
	assert(next != _NULL);
#endif
	//Set value.
	next->previous = previous;
}

////////////////////////////////////////////////////////////////////////////////
//
// SetPreviousNext
//
////////////////////////////////////////////////////////////////////////////////
	
void CListNode::setPreviousNext(CListNode* next)
{
#ifdef _DEBUG
	assert(previous != _NULL);
#endif
	//Set value.
	previous->next = next;
}
