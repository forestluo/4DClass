#include "4DNode.h"

///////////////////////////////////////////////////////////////////////////////
//
// C4DNode
//
///////////////////////////////////////////////////////////////////////////////

C4DNode::C4DNode(void)
{
	//Set default value.
	value = _NULL;
}

///////////////////////////////////////////////////////////////////////////////
//
// C4DNode
//
///////////////////////////////////////////////////////////////////////////////

C4DNode::C4DNode(_OBJECT value)
{
	//Set default value.
	this->value = value;
}

///////////////////////////////////////////////////////////////////////////////
//
// ~C4DNode
//
///////////////////////////////////////////////////////////////////////////////

C4DNode::~C4DNode(void)
{
	//Clear value.
	value = _NULL;
}

///////////////////////////////////////////////////////////////////////////////
//
// ClearValue
//
///////////////////////////////////////////////////////////////////////////////

void C4DNode::clearValue()
{
	//Clear value.
	value = _NULL;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetValue
//
///////////////////////////////////////////////////////////////////////////////

_OBJECT C4DNode::getValue()
{
	//Return result.
	return value;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetValue
//
///////////////////////////////////////////////////////////////////////////////

void C4DNode::setValue(_OBJECT value)
{
	//Set value.
	this->value = value;
}
