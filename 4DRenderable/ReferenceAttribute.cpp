#include "ReferenceAttribute.h"

///////////////////////////////////////////////////////////////////////////////
//
// CReferenceAttribute
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CReferenceAttribute::CReferenceAttribute(void)
{
	//Set default value.
	referenced = _FALSE;
}

///////////////////////////////////////////////////////////////////////////////
//
// CReferenceAttribute
//
// Default deconstruction.
//
/////////////////////////////////////////////////////////////////////////////////

CReferenceAttribute::~CReferenceAttribute(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// IsReferenced
//
///////////////////////////////////////////////////////////////////////////////

_BOOL CReferenceAttribute::isReferenced() const
{
	//Return result.
	return referenced;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetReferenced
//
///////////////////////////////////////////////////////////////////////////////

void CReferenceAttribute::setReferenced(_BOOL referenced)
{
	//Set value.
	this->referenced = referenced;
}
