#include "BoxRecycleBin.h"
#include "SimpleBox.h"

///////////////////////////////////////////////////////////////////////////////
//
// Definition
//
///////////////////////////////////////////////////////////////////////////////

#define MAX_BOX_COUNT										2048

///////////////////////////////////////////////////////////////////////////////
//
// CBoxRecycleBin
//
///////////////////////////////////////////////////////////////////////////////

CBoxRecycleBin::CBoxRecycleBin(void)
{
	//Set default capacity.
	setCapacity(MAX_BOX_COUNT);
}

///////////////////////////////////////////////////////////////////////////////
//
// ~CBoxRecycleBin
//
///////////////////////////////////////////////////////////////////////////////

CBoxRecycleBin::~CBoxRecycleBin(void)
{
	//Clear all.
	clearAll();
}

///////////////////////////////////////////////////////////////////////////////
//
// NewObject
//
///////////////////////////////////////////////////////////////////////////////

_OBJECT CBoxRecycleBin::newObject()
{
	//Return result.
	return new CSimpleBox();
}

///////////////////////////////////////////////////////////////////////////////
//
// DeleteObject
//
///////////////////////////////////////////////////////////////////////////////

void CBoxRecycleBin::deleteObject(_OBJECT object)
{
#ifdef _DEBUG
	assert(object != _NULL);
#endif
	//Delete object.
	delete ((CSimpleBox *)object);
}

///////////////////////////////////////////////////////////////////////////////
//
// Recycle
//
///////////////////////////////////////////////////////////////////////////////

void CBoxRecycleBin::recycle(_OBJECT object)
{
#ifdef _DEBUG
	assert(object != _NULL);
#endif
	//Recycle box.
	CRecycleBin::recycle((CSimpleBox *)object);
}
