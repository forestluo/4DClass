#include "JointRelationAttribute.h"

///////////////////////////////////////////////////////////////////////////////
//
// CJointRelationAttribute
//
// Default construction.
//
///////////////////////////////////////////////////////////////////////////////

CJointRelationAttribute::CJointRelationAttribute(void)
{
	//Set default value.
	child = (CWorldObject *)_NULL;
	parent = (CWorldObject *)_NULL;
}

///////////////////////////////////////////////////////////////////////////////
//
// CJointRelationAttribute
//
// Default deconstruction.
//
///////////////////////////////////////////////////////////////////////////////

CJointRelationAttribute::~CJointRelationAttribute(void)
{
	//Clear value.
	child = (CWorldObject *)_NULL;
	parent = (CWorldObject *)_NULL;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetJointChild
//
///////////////////////////////////////////////////////////////////////////////

CWorldObject* CJointRelationAttribute::getJointChild() const
{
	//Return result.
	return child;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetJointChild
//
///////////////////////////////////////////////////////////////////////////////

void CJointRelationAttribute::setJointChild(CWorldObject* child)
{
#ifdef _DEBUG
	assert(child != _NULL);
#endif
	//Set value.
	this->child = child;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetJointParent
//
///////////////////////////////////////////////////////////////////////////////

CWorldObject* CJointRelationAttribute::getJointParent() const
{
	//Return result.
	return parent;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetJointParent
//
///////////////////////////////////////////////////////////////////////////////

void CJointRelationAttribute::setJointParent(CWorldObject* parent)
{
#ifdef _DEBUG
	assert(parent != _NULL);
#endif
	//Set value.
	this->parent = parent;
}
