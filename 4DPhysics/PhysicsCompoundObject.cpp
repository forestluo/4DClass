#include "PhysicsCompoundObject.h"

///////////////////////////////////////////////////////////////////////////////
//
// CPhysicsCompoundObject
//
// Default construction.
//
///////////////////////////////////////////////////////////////////////////////

CPhysicsCompoundObject::CPhysicsCompoundObject(void)
{
	//Set this.
	setThis(this);
	//Set object type.
	setObjectType(CTypeAttribute::compoundObject);
}

///////////////////////////////////////////////////////////////////////////////
//
// CPhysicsCompoundObject
//
// Default deconstruction.
//
///////////////////////////////////////////////////////////////////////////////

CPhysicsCompoundObject::~CPhysicsCompoundObject(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// AddObject
//
///////////////////////////////////////////////////////////////////////////////

void CPhysicsCompoundObject::addObject(CPhysicsObject* object)
{
#ifdef _DEBUG
	assert(object != _NULL);
	//The joint object cannot be a child object.
	assert(object->getObjectType() != CTypeAttribute::jointObject);
#endif
	//Add object.
	CWorldObject::addObject(object);
}
