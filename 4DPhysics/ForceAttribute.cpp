///////////////////////////////////////////////////////////////////////////////
//
// CForceAttribute
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CForceAttribute::CForceAttribute(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// CForceAttribute
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CForceAttribute::CForceAttribute(const CForceAttribute& attribute)
{
	//Set force.
	force = attribute.force;
	//Set torque.
	torque = attribute.torque;

	//Set alpha.
	alpha = attribute.alpha;
	//Set omega.
	omega = attribute.omega;

	//Set velocity.
	velocity = attribute.velocity;
	//Set acceleration.
	acceleration = attribute.acceleration;
}

///////////////////////////////////////////////////////////////////////////////
//
// CForceAttribute
//
// Default deconstruction.
//
/////////////////////////////////////////////////////////////////////////////////

CForceAttribute::~CForceAttribute(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// Operator
//
/////////////////////////////////////////////////////////////////////////////////

CForceAttribute& CForceAttribute::operator = (const CForceAttribute& attribute)
{
	//Set force.
	force = attribute.force;
	//Set torque.
	torque = attribute.torque;

	//Set alpha.
	alpha = attribute.alpha;
	//Set omega.
	omega = attribute.omega;

	//Set velocity.
	velocity = attribute.velocity;
	//Set acceleration.
	acceleration = attribute.acceleration;

	//Return this.
	return *this;
}

