#include "StatusAttribute.h"

///////////////////////////////////////////////////////////////////////////////
//
// CStatusAttribute
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CStatusAttribute::CStatusAttribute(void)
{
	//Set default value.
	timestamp = 0L;
}

///////////////////////////////////////////////////////////////////////////////
//
// CStatusAttribute
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CStatusAttribute::CStatusAttribute(const CStatusAttribute& attribute)
{
	//Set timestamp.
	timestamp = attribute.timestamp;
	
	//Set omega.
	omega = attribute.omega;
	//Set velocity.
	velocity = attribute.velocity;

	//Set scale.
	scale = attribute.scale;
	//Set position.
	position = attribute.position;
	//Set rotation.
	rotation = attribute.rotation;
}

///////////////////////////////////////////////////////////////////////////////
//
// CStatusAttribute
//
// Default deconstruction.
//
/////////////////////////////////////////////////////////////////////////////////

CStatusAttribute::~CStatusAttribute(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// Operator
//
/////////////////////////////////////////////////////////////////////////////////

CStatusAttribute& CStatusAttribute::operator = (const CStatusAttribute& attribute)
{
	//Set timestamp.
	timestamp = attribute.timestamp;
	
	//Set omega.
	omega = attribute.omega;
	//Set velocity.
	velocity = attribute.velocity;

	//Set scale.
	scale = attribute.scale;
	//Set position.
	position = attribute.position;
	//Set rotation.
	rotation = attribute.rotation;

	//Return this.
	return *this;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetScale
//
///////////////////////////////////////////////////////////////////////////////

const C4DVector3& CStatusAttribute::getScale() const
{
	//Return result.
	return scale;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetScale
//
///////////////////////////////////////////////////////////////////////////////

void CStatusAttribute::setScale(const C4DVector3& scale)
{
	//Set value.
	this->scale = scale;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetPosition
//
///////////////////////////////////////////////////////////////////////////////

const C4DVector3& CStatusAttribute::getPosition() const
{
	//Return result.
	return position;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetPosition
//
///////////////////////////////////////////////////////////////////////////////

void CStatusAttribute::setPosition(const C4DVector3& position)
{
	//Set value.
	this->position = position;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetRotation
//
///////////////////////////////////////////////////////////////////////////////

const C4DQuaternion& CStatusAttribute::getRotation() const
{
	//Return result.
	return rotation;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetRotate
//
///////////////////////////////////////////////////////////////////////////////

void CStatusAttribute::setRotation(const C4DQuaternion& rotation)
{
	//Set rotation.
	this->rotation = rotation;
}
