#include "D3DXResource.h"

///////////////////////////////////////////////////////////////////////////////
//
// Global
//
///////////////////////////////////////////////////////////////////////////////

//Set base of object.
const C4DBase CD3DXResource::base(L"CD3DXResource",L"CD3DXObject");

///////////////////////////////////////////////////////////////////////////////
//
// CD3DXResource
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CD3DXResource::CD3DXResource(void)
{
	//Set default value.
	reference = 0;
	reserved = _FALSE;
}

///////////////////////////////////////////////////////////////////////////////
//
// CD3DXResource
//
// Default deconstruction.
//
/////////////////////////////////////////////////////////////////////////////////

CD3DXResource::~CD3DXResource(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// GetName
//
///////////////////////////////////////////////////////////////////////////////

const CNormalString& CD3DXResource::getName() const
{
	//Return result.
	return name;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetName
//
///////////////////////////////////////////////////////////////////////////////

void CD3DXResource::setName(const CNormalString& name)
{
	//Set value.
	this->name = name;
}

///////////////////////////////////////////////////////////////////////////////
//
// IsReserved
//
///////////////////////////////////////////////////////////////////////////////

_BOOL CD3DXResource::isReserved() const
{
	//Return result.
	return reserved;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetReserved
//
///////////////////////////////////////////////////////////////////////////////

void CD3DXResource::setReserved(_BOOL reserved)
{
	//Set value.
	this->reserved = reserved;
}

///////////////////////////////////////////////////////////////////////////////
//
// AddReference
//
///////////////////////////////////////////////////////////////////////////////

void CD3DXResource::addReference()
{
#ifdef _DEBUG
	assert(reference >= 0);
#endif
	//Add reference.
	reference ++;
}

///////////////////////////////////////////////////////////////////////////////
//
// ReleaseReference
//
///////////////////////////////////////////////////////////////////////////////

void CD3DXResource::releaseReference()
{
#ifdef _DEBUG
	assert(reference > 0);
#endif
	//Release reference
	reference --;
}

///////////////////////////////////////////////////////////////////////////////
//
// CountReference
//
///////////////////////////////////////////////////////////////////////////////

_INTEGER CD3DXResource::countReference() const
{
	//Return result.
	return reference;
}
