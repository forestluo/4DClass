#ifndef _D3DX_VERTEX_H_
#define _D3DX_VERTEX_H_

#pragma once

///////////////////////////////////////////////////////////////////////////////
//
// Including
//
///////////////////////////////////////////////////////////////////////////////

#include "4DType.h"

///////////////////////////////////////////////////////////////////////////////
//
// Vertex0
//
typedef struct tag4DVERTEX0
{
	//Position
	_FLOAT _x,_y,_z;

	//Contruction
	tag4DVERTEX0(_FLOAT x,_FLOAT y,_FLOAT z)
		: _x(x),_y(y),_z(z) {}

} _4DVERTEX0;
//
#define D3DFVF_4DVERTEX0 (D3DFVF_XYZ)
//
///////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////
//
// Vertex1
//
typedef struct tag4DVERTEX1 : public tag4DVERTEX0
{
	//Color.
	_INTEGER _color;

	//Contruction
	tag4DVERTEX1(_FLOAT x,_FLOAT y,_FLOAT z,_INTEGER color)
		: tag4DVERTEX0(x,y,z),_color(color) {}

} _4DVERTEX1;
//
#define D3DFVF_4DVERTEX1 (D3DFVF_XYZ | D3DFVF_DIFFUSE)
//
///////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////
//
// Vertex2
//
typedef struct tag4DVERTEX2 : public tag4DVERTEX0
{
	//Normal
	_FLOAT _nx,_ny,_nz;

	//Contruction
	tag4DVERTEX2(_FLOAT x,_FLOAT y,_FLOAT z,_FLOAT nx,_FLOAT ny,_FLOAT nz)
		: tag4DVERTEX0(x,y,z),_nx(nx),_ny(ny),_nz(nz) {}

} _4DVERTEX2;
//
#define D3DFVF_4DVERTEX2 (D3DFVF_XYZ | D3DFVF_NORMAL)
//
///////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////
//
// Vertex3
//
typedef struct tag4DVERTEX3 : public tag4DVERTEX2
{
	//Texture
	_FLOAT _u,_v;

	//Contruction
	tag4DVERTEX3(_FLOAT x,_FLOAT y,_FLOAT z,_FLOAT nx,_FLOAT ny,_FLOAT nz,_FLOAT u,_FLOAT v)
		: tag4DVERTEX2(x,y,z,nx,ny,nz),_u(u),_v(v) {}

} _4DVERTEX3;
//
#define D3DFVF_4DVERTEX3 (D3DFVF_XYZ | D3DFVF_NORMAL | D3DFVF_TEX1)
//
///////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////
#endif //_D3DX_VERTEX_H_