#include "D3DXEvent.h"

///////////////////////////////////////////////////////////////////////////////
//
// IsMouseEvent
//
///////////////////////////////////////////////////////////////////////////////

bool tagD3DXMsgProcEvent::IsMouseEvent() const
{
	//Check msg.
	switch(uMsg)
	{
	case WM_MOUSEMOVE:
	case WM_MOUSEWHEEL:

	case WM_MBUTTONUP:
	case WM_MBUTTONDOWN:
	case WM_MBUTTONDBLCLK:

	case WM_LBUTTONUP:
	case WM_LBUTTONDOWN:
	case WM_LBUTTONDBLCLK:

	case WM_RBUTTONUP:
	case WM_RBUTTONDOWN:
	case WM_RBUTTONDBLCLK:
		//Return true.
		return true;
	}
	//Return false.
	return false;
}

///////////////////////////////////////////////////////////////////////////////
//
// IsMousePickEvent
//
///////////////////////////////////////////////////////////////////////////////

bool tagD3DXMsgProcEvent::IsMousePickEvent() const
{
	//Return result.
	return uMsg == WM_LBUTTONDOWN;
}

///////////////////////////////////////////////////////////////////////////////
//
// IsMouseReleaseEvent
//
///////////////////////////////////////////////////////////////////////////////

bool tagD3DXMsgProcEvent::IsMouseReleaseEvent() const
{
	//Return result.
	return uMsg == WM_LBUTTONUP;
}

///////////////////////////////////////////////////////////////////////////////
//
// IsMoveObjectEvent
//
///////////////////////////////////////////////////////////////////////////////

bool tagD3DXMsgProcEvent::IsMouseMoveObjectEvent() const
{
	//Return result.
	return uMsg == WM_MOUSEMOVE && (wParam & MK_LBUTTON) && !(wParam & MK_CONTROL);
}
