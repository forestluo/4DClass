//ID Number
_INTEGER CIdentityAttribute::objectNumber					= 0;

///////////////////////////////////////////////////////////////////////////////
//
// CIdentityAttribute
//
// Default construction.
//
/////////////////////////////////////////////////////////////////////////////////

CIdentityAttribute::CIdentityAttribute(void)
{
	//Get object id.
	objectID = ++ objectNumber;
	//Set default alias.
	alias.append(L"ÎïÌå")->append(objectID);
}

///////////////////////////////////////////////////////////////////////////////
//
// CIdentityAttribute
//
// Default deconstruction.
//
/////////////////////////////////////////////////////////////////////////////////

CIdentityAttribute::~CIdentityAttribute(void)
{

}

///////////////////////////////////////////////////////////////////////////////
//
// GetID
//
///////////////////////////////////////////////////////////////////////////////

_INTEGER CIdentityAttribute::getID() const
{
	//Return result.
	return objectID;
}

///////////////////////////////////////////////////////////////////////////////
//
// GetAlias
//
///////////////////////////////////////////////////////////////////////////////

const CNormalString& CIdentityAttribute::getAlias() const
{
	//Return result.
	return alias;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetAlias
//
///////////////////////////////////////////////////////////////////////////////

void CIdentityAttribute::setAlias(const CNormalString& alias)
{
#ifdef _DEBUG
	assert(alias.length() > 0);
#endif
	//Set alias.
	this->alias = alias;
}

///////////////////////////////////////////////////////////////////////////////
//
// SetAlias
//
///////////////////////////////////////////////////////////////////////////////

void CIdentityAttribute::setAlias(const CNormalString& prefix,const CNormalString& suffix)
{
	//Clear all.
	alias.clearAll();
	//Append.
	alias.append(prefix.getCString())->append(objectID)->append(suffix.getCString());
}
