class CCollisionAttribute :
	public virtual CWorldGroup
{
public:
	CCollisionAttribute(void);
	virtual ~CCollisionAttribute(void);

public:

	/**
	 * Clear collision.
	 *
	 * @param None No parameters needed.
	 * @return
	 *     <p>No results returned.</p>
	 */
	void clearCollision();

	/**
	 * Update collision.
	 *
	 * @param object Renderable object.
	 * @return
	 *     <p>No results returned.</p>
	 */
	void updateCollision(CWorldObject* object);

};

